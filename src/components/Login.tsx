export default function SignInSide() {

  const classes = useStyles();
  const [account, setAccount] = React.useState({ username: "", password: "" });
  // const userState = useAppSelector(state => state.users)
  const dispatch = useAppDispatch()

  const handelAccount = (property: string, event: React.ChangeEvent<HTMLTextAreaElement | HTMLInputElement>) => {
    setAccount({ ...account, [property]: event.target.value });
  }

  const handelLogin = async () => {
    console.log('here')
    // dispatch(loginUser({ username: account.username, password: account.password }));
    const isUserOrUndef = await authService.login(account.username, account.password);
    console.log('isUserOrUndef', isUserOrUndef)
    if (isUserOrUndef) {
      dispatch(login({ UserResponse: isUserOrUndef }))
      // props.history.push("/home");
    }
    // setAccount({ username: "", password: "" });
    // 
    console.log('end')
  };

  return (
    <Grid container component="main" className={classes.root}>
      <CssBaseline />
      {/* <Grid item xs={false} sm={4} md={7} className={classes.image} /> */}
      <Grid
        className={classes.size}
        item
        xs={12}
        sm={8}
        md={8}
        component={Paper}
        elevation={1}
        square
      >
        <div className={classes.paper}>
          <Avatar className={classes.avatar}>
            <LockOutlinedIcon />
          </Avatar>
          <Typography component="h1" variant="h5">
            Sign in
          </Typography>
          <div className={classes.form}>
            <TextField
              onChange={(event) => handelAccount("username", event)}
              variant="outlined"
              margin="normal"
              required
              fullWidth
              id="username"
              label="Username"
              name="username"
              autoFocus
            />
            <TextField
              onChange={(event) => handelAccount("password", event)}
              variant="outlined"
              margin="normal"
              required
              fullWidth
              name="password"
              label="Password"
              type="password"
              id="password"
              autoComplete="current-password"
            />
            <FormControlLabel
              control={<Checkbox value="remember" color="primary" />}
              label="Remember me"
            />
            <Button
              type="submit"
              fullWidth
              variant="contained"
              color="primary"
              className={classes.submit}
              onClick={handelLogin}
            >
              Sign In
            </Button>
            <Grid container>
              <Grid item>
                <Link href="#" variant="body2">
                  {"Don't have an account? Sign Up"}
                </Link>
              </Grid>
            </Grid>
            <Box mt={5}>
              <Copyright />
            </Box>
          </div>
        </div>
      </Grid>
    </Grid>
  );
}
